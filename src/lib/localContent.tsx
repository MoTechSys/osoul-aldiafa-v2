function otherCitiesLinks(service: string, currentCity: string) {
  return LOCAL_PAGES.filter(
    (p) => p.service === service && p.city !== currentCity
  ).map((p) => ({
    label: `${SERVICES[service].ar} ${CITIES[p.city].ar}`,
    href: `/${localSlug(service, p.city)}`,
  }));
}

